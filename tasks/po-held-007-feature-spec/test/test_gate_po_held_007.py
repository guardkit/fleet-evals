def test_auto_mode_discipline(manifest):
    """The SPL-007 headless pin (contract A.2): >=3 assumptions on a thin
    brief, every confidence 'low', every human_response 'deferred' (stricter
    than the vanilla --auto 'confirmed' — the PO never silently accepts), and
    review_required true."""
    entries = manifest.get("assumptions") or []
    assert len(entries) >= MIN_ASSUMPTIONS, (
        f"the brief has deliberate unknowns; expected >={MIN_ASSUMPTIONS} assumptions, got {len(entries)}"
    )
    for e in entries:
        assert e.get("confidence") == "low", (
            f"{e.get('id')}: confidence {e.get('confidence')!r} — --auto forces every confidence to low"
        )
        assert e.get("human_response") == "deferred", (
            f"{e.get('id')}: human_response {e.get('human_response')!r} — the SPL pin is 'deferred'"
        )
    assert manifest.get("review_required") is True, "--auto output must set review_required: true"


def test_summary_coherence(parsed, manifest, summary_text, paths):
    """Part A.3 internal consistency: header counts agree with .feature tag
    counts and the manifest; Review required 'Yes' iff review_required iff >=1
    low; the four pinned category-table rows agree (additive rows allowed);
    the /feature-plan integration line names this triple's summary path."""
    s = spec_gates.parse_summary(summary_text)
    n_scen = len(parsed["scenarios"])
    assert s["scenarios"] is not None, "summary missing '**Scenarios**: N total (S smoke, R regression)'"
    total, smoke, regression = map(int, s["scenarios"])
    assert total == n_scen, f"summary says {total} scenarios, .feature has {n_scen}"
    assert smoke == spec_gates.tag_count(parsed, "@smoke"), "summary smoke count disagrees with tags"
    assert regression == spec_gates.tag_count(parsed, "@regression"), "summary regression count disagrees with tags"

    entries = manifest.get("assumptions") or []
    assert s["assumptions"] is not None, "summary missing '**Assumptions**: N total (h/m/l)'"
    a_total, high, medium, low = map(int, s["assumptions"])
    by_conf = {c: sum(1 for e in entries if e.get("confidence") == c) for c in ("high", "medium", "low")}
    assert a_total == len(entries), f"summary says {a_total} assumptions, manifest has {len(entries)}"
    assert (high, medium, low) == (by_conf["high"], by_conf["medium"], by_conf["low"]), (
        f"summary breakdown {high}/{medium}/{low} disagrees with manifest {by_conf}"
    )

    review_yes = s["review_required"] == "Yes"
    assert review_yes == bool(manifest.get("review_required")) == (by_conf["low"] > 0), (
        f"Review required '{s['review_required']}' vs manifest review_required="
        f"{manifest.get('review_required')} vs low-count={by_conf['low']}"
    )

    for tag, count in s["category_counts"].items():
        assert count is not None, f"summary category table missing the pinned {tag} row"
        assert count == spec_gates.tag_count(parsed, tag), (
            f"summary {tag} count {count} disagrees with tag count {spec_gates.tag_count(parsed, tag)}"
        )

    slug = paths["slug"].name
    assert f"features/{slug}/{slug}_summary.md" in summary_text, (
        "Integration section must name this triple's summary as the /feature-plan --context path"
    )


