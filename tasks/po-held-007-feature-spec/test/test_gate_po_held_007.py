def test_feature_header_block(parsed, manifest):
    """The five pinned header comments, with the assumption count agreeing
    with the manifest and a FULL ISO-8601 timestamp (bare dates are a gold-
    trace deviation new output must not reproduce, contract Part C)."""
    header = spec_gates.header_block(parsed)
    assert header.get("generated_by", "").startswith("Generated by /feature-spec"), \
        "missing '# Generated by /feature-spec' header comment"
    for key in ("feature", "stack", "assumptions", "generated"):
        assert key in header, f"missing '# {key.title()}:' header comment"
    assert spec_gates.normalize(header["feature"]) == spec_gates.normalize(parsed["feature"]["name"]), (
        f"header Feature {header['feature']!r} != Feature: line {parsed['feature']['name']!r}"
    )
    n = len(manifest.get("assumptions") or [])
    assert header["assumptions"].split()[0] == str(n), (
        f"header '# Assumptions: {header['assumptions']}' disagrees with manifest count {n}"
    )
    assert spec_gates.is_full_iso_timestamp(header["generated"]), (
        f"'# Generated: {header['generated']}' is not a full ISO 8601 timestamp"
    )


