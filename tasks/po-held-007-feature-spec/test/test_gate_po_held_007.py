def test_gherkin_structure(feature_text, parsed):
    """One Feature block with the As a / I want / So that story; every
    scenario has steps and at least one Then; UTF-8 without BOM; no
    structural parse findings (wrapped steps have their own gate)."""
    assert not feature_text.startswith("\ufeff"), "UTF-8 BOM — contract pins 'no BOM'"
    structural = [f for f in parsed["findings"] if f["defect"] != "wrapped_step_continuation"]
    assert structural == [], "\n" + "\n".join(json.dumps(f) for f in structural)
    assert parsed["scenarios"], "no scenarios in .feature"
    story = " ".join(parsed["feature"]["description"]).lower()
    for prefix in ("as a", "i want", "so that"):
        assert prefix in story, f"feature story missing '{prefix} ...' line"
    for sc in parsed["scenarios"]:
        assert sc["steps"], f"scenario {sc['name']!r}: no steps"
        assert any(s.split(" ", 1)[0] == "Then" for s in sc["steps"]), (
            f"scenario {sc['name']!r}: no Then step (no observable outcome)"
        )


